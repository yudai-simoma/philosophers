#include "philosophers.h"

/*
** 引数c が数字であれば 0以外を返し、そうでなければ 0 を返す。
*/
int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	else
		return (0);
}
